
public class OndalikSayiHassasiyeti {
public static void main (String[] args) {
	
	System.out.println("1.0/3.0 = " + 1.0/3.0);
	
	System.out.println("1.0F/3.0F = " + 1.0F/3.0F);
	
	/*1.0 / 3.0 ifadesinde, 1.0 ve 3.0 ondalık sayı literalleridir ve bu nedenle bu ifade double veri tipinde bir bölme işlemi gerçekleştirir.
	 Sonuç da double veri tipindedir.

	1.0F / 3.0F ifadesinde ise, sayıların sonuna eklenen "F" harfi, ondalık sayı literallerini float veri tipine dönüştürür. 
	Bu durumda ifade float veri tipinde bir bölme işlemi yapar ve sonuç da float veri tipindedir.
	*/

}
}
